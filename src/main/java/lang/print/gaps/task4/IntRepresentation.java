package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char first = 'c';;
        char second = 'h';
        char third = 'a';
        char fourth = 'r';
        int c = first;
        int h = second;
        int a = third;
        int r = fourth;
        System.out.println(c + h + a + r);

    }
}
